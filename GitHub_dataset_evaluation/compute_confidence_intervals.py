#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Compute bootstrap confidence intervals for evaluation metrics.

This script reads the _per_username.csv files generated by evaluate_gitAuthority.py
and computes confidence intervals for recall, precision, F1, and other metrics.
"""

import sys
import pandas as pd
import numpy as np
from scipy import stats
import argparse
import os


def bootstrap_ci(data, n_bootstrap=10000, ci=95, seed=42):
    """
    Compute bootstrap confidence interval for the mean of data.

    Args:
        data: Array-like of values
        n_bootstrap: Number of bootstrap samples (default: 10000)
        ci: Confidence level in percent (default: 95)
        seed: Random seed for reproducibility (default: 42)

    Returns:
        tuple: (mean, lower_bound, upper_bound)
    """
    np.random.seed(seed)

    data = np.array(data)
    n = len(data)

    # Generate bootstrap samples
    bootstrap_means = np.zeros(n_bootstrap)
    for i in range(n_bootstrap):
        # Sample with replacement
        bootstrap_sample = np.random.choice(data, size=n, replace=True)
        bootstrap_means[i] = np.mean(bootstrap_sample)

    # Calculate percentiles
    alpha = (100 - ci) / 2
    lower = np.percentile(bootstrap_means, alpha)
    upper = np.percentile(bootstrap_means, 100 - alpha)
    mean = np.mean(data)

    return mean, lower, upper


def normal_ci(data, ci=95):
    """
    Compute confidence interval using normal approximation (t-distribution).

    Args:
        data: Array-like of values
        ci: Confidence level in percent (default: 95)

    Returns:
        tuple: (mean, lower_bound, upper_bound)
    """
    data = np.array(data)
    n = len(data)
    mean = np.mean(data)
    std_err = stats.sem(data)  # Standard error of the mean

    # Use t-distribution for confidence interval
    alpha = (100 - ci) / 100
    ci_range = std_err * stats.t.ppf(1 - alpha/2, n - 1)

    lower = mean - ci_range
    upper = mean + ci_range

    return mean, lower, upper


def compute_cis_for_metrics(df, metrics, n_bootstrap=10000, ci=95, seed=42):
    """
    Compute confidence intervals for multiple metrics.

    Args:
        df: DataFrame with per-username metrics
        metrics: List of metric column names
        n_bootstrap: Number of bootstrap samples
        ci: Confidence level in percent
        seed: Random seed

    Returns:
        dict: Results for each metric with bootstrap and normal CIs
    """
    results = {}

    for metric in metrics:
        if metric not in df.columns:
            print(f"Warning: Metric '{metric}' not found in data. Skipping.")
            continue

        # Remove any NaN values
        data = df[metric].dropna().values

        if len(data) == 0:
            print(f"Warning: No valid data for metric '{metric}'. Skipping.")
            continue

        print(f"\nComputing CIs for {metric}...")
        print(f"  Sample size: {len(data):,}")

        # Bootstrap CI
        boot_mean, boot_lower, boot_upper = bootstrap_ci(
            data, n_bootstrap=n_bootstrap, ci=ci, seed=seed
        )

        # Normal CI
        norm_mean, norm_lower, norm_upper = normal_ci(data, ci=ci)

        results[metric] = {
            'n': len(data),
            'mean': boot_mean,
            'std': np.std(data),
            'median': np.median(data),
            'bootstrap_lower': boot_lower,
            'bootstrap_upper': boot_upper,
            'normal_lower': norm_lower,
            'normal_upper': norm_upper
        }

    return results


def print_results(results, ci):
    """Print results in a formatted table."""
    print(f"\n{'='*80}")
    print(f"CONFIDENCE INTERVALS ({ci}% confidence level)")
    print(f"{'='*80}")
    print(f"\n{'Metric':<20} {'Mean':<10} {'Std Dev':<10} {'Bootstrap CI':<25} {'Normal CI':<25}")
    print(f"{'-'*20} {'-'*10} {'-'*10} {'-'*25} {'-'*25}")

    for metric, vals in results.items():
        boot_ci = f"[{vals['bootstrap_lower']:.4f}, {vals['bootstrap_upper']:.4f}]"
        norm_ci = f"[{vals['normal_lower']:.4f}, {vals['normal_upper']:.4f}]"
        print(f"{metric:<20} {vals['mean']:<10.4f} {vals['std']:<10.4f} {boot_ci:<25} {norm_ci:<25}")

    print(f"\n{'='*80}")


def save_results(results, output_file, ci):
    """Save results to CSV file."""
    rows = []
    for metric, vals in results.items():
        rows.append({
            'metric': metric,
            'n': vals['n'],
            'mean': vals['mean'],
            'std': vals['std'],
            'median': vals['median'],
            f'bootstrap_ci_{ci}_lower': vals['bootstrap_lower'],
            f'bootstrap_ci_{ci}_upper': vals['bootstrap_upper'],
            f'normal_ci_{ci}_lower': vals['normal_lower'],
            f'normal_ci_{ci}_upper': vals['normal_upper']
        })

    df = pd.DataFrame(rows)
    df.to_csv(output_file, index=False)
    print(f"\nResults saved to: {output_file}")


def main():
    parser = argparse.ArgumentParser(
        description='Compute bootstrap confidence intervals for evaluation metrics'
    )
    parser.add_argument(
        'input_csv',
        help='Path to _per_username.csv file from evaluate_gitAuthority.py (or from any other evaluation script in this directory)'
    )
    parser.add_argument(
        '-m', '--metrics',
        nargs='+',
        default=['recall', 'precision', 'f1', 'accuracy', 'balanced_accuracy',
                 'splitting', 'lumping', 'merge_percentage'],
        help='Metrics to compute CIs for (default: all metrics)'
    )
    parser.add_argument(
        '-n', '--n-bootstrap',
        type=int,
        default=10000,
        help='Number of bootstrap samples (default: 10000)'
    )
    parser.add_argument(
        '-c', '--confidence',
        type=int,
        default=95,
        help='Confidence level in percent (default: 95)'
    )
    parser.add_argument(
        '-s', '--seed',
        type=int,
        default=42,
        help='Random seed for reproducibility (default: 42)'
    )
    parser.add_argument(
        '-o', '--output',
        help='Output CSV file for results (optional)'
    )

    args = parser.parse_args()

    # Check if input file exists
    if not os.path.exists(args.input_csv):
        print(f"Error: Input file '{args.input_csv}' not found")
        sys.exit(1)

    # Load data
    print(f"Loading data from {args.input_csv}...")
    df = pd.read_csv(args.input_csv)
    print(f"Loaded {len(df):,} GitHub usernames")

    # Compute confidence intervals
    print(f"\nComputing {args.confidence}% confidence intervals using {args.n_bootstrap:,} bootstrap samples...")
    results = compute_cis_for_metrics(
        df,
        metrics=args.metrics,
        n_bootstrap=args.n_bootstrap,
        ci=args.confidence,
        seed=args.seed
    )

    # Print results
    print_results(results, args.confidence)

    # Save to file if requested
    if args.output:
        save_results(results, args.output, args.confidence)
    elif args.input_csv.endswith('_per_username.csv'):
        # Auto-generate output filename
        output_file = args.input_csv.replace('_per_username.csv', '_confidence_intervals.csv')
        save_results(results, output_file, args.confidence)


if __name__ == "__main__":
    main()
